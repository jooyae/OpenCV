CCalculateRoot::CCalculateRoot()
{
}


CCalculateRoot::~CCalculateRoot()
{
}


int CCalculateRoot::CalcRoot1(double a, double b)
{
	return a+b;
}


int CCalculateRoot::CalcRoot2(double a, double b)
{
	return a-b;
}


int CCalculateRoot::CalcRoot3(double a, double b)
{
	return a*b;
}


int CCalculateRoot::CalcRoot4(double a, double b)
{
	return a/b;
}
